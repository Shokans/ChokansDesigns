// navbar-brand
document.getElementById("navbar-brand").addEventListener("mouseover", function mouseOver() {
    document.getElementById("navbar-brand").style.color = "white";
  });
document.getElementById("navbar-brand").addEventListener("mouseout", mouseOut);

function mouseOver() {
  document.getElementById("navbar-brand").style.color = "white";
}

function mouseOut() {
  document.getElementById("navbar-brand").style.color = "#134361";
}
